pub trait OperatorInfo {
    fn ends_basic_block(&self) -> bool;
    fn code(&self) -> OperatorCode;
}

impl OperatorInfo for Operator<'_> {
    fn ends_basic_block(&self) -> bool {
        use Operator::*;

        macro_rules! dot {
            ($first:ident $(,$opcode:ident)*) => {
                $first { .. } $(| $opcode { .. })*
            };
        }

        matches!(
            self,
            End | Else | Return | dot!(Loop, Br, BrTable, BrIf, If, Call, CallIndirect)
        )
    }

    fn code(&self) -> OperatorCode {
        self.into()
    }
}
